package com.array;

public class Arrays {
	public static void main(String[] args)
	{
	int[] a={10,20,30,40};
	System.out.println(a[0]);
	System.out.println(a[1]);
	System.out.println(a[2]);
	System.out.println(a[3]);
	
	int[] a2={10,20,30,40};
	for (int i=0;i<a2.length;i++)
	{
	System.out.println(a2[i]);
	}
	}
}
